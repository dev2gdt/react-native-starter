function parseIconFromClassName(iconClassName) {
  if (!iconClassName) return;

  const Icons = iconClassName.indexOf('far') > -1 ? RegularIcons :
                iconClassName.indexOf('fab') > -1 ? BrandIcons :
                iconClassName.indexOf('fal') > -1 ? LightIcons :
                SolidIcons

  iconClassName = iconClassName.replace(/(fa\-)/gi, '')
  iconClassName = iconClassName.replace(/(fa|fas|far|fab)( )/gi, '')

  let nameParts = iconClassName.match(/(\-)(\w{1,1})/gi) || []

  nameParts.forEach(m => {
    iconClassName = iconClassName.replace(m, m.toUpperCase())
  })

  iconClassName = iconClassName.replace(/\-/gi, '') 
  iconClassName = (iconClassName || '').trim()

  return Icons[iconClassName]
}

